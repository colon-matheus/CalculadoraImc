package control;

public class Pessoa {
	
	public int idade;
	public String sexo;
	public boolean gravida;
	public double altura;
	public double peso;

	public Pessoa(int idade, String sexo, boolean gravida, double altura, double peso) {
		this.idade = idade;
		this.sexo = sexo;
		this.altura = altura;
		this.peso = peso;
		this.gravida = gravida;
	}

}
